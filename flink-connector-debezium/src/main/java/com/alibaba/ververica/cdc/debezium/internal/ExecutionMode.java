package com.alibaba.ververica.cdc.debezium.internal;

/** The mode to indicate the processor in which phase. */
public enum ExecutionMode {
    /** The snapshot phase is atomic. If the snapshot fails, we should start from the beginning. */
    SNAPSHOT,
    /**
     * The streaming phase processes the records from the binlog and commit the offset when
     * checkpoint finishes.
     */
    STREAMING;
}
